package com.example.demo.logic;

public class Swaping {

	public static void main(String[] args) {
		
		
		int a=10;
		int b=20;
		System.out.println("before swaping values "+a+ " "+b);
		
		a=a+b;//30
		b=a-b;//20
		a=a-b;
		
		System.out.println("after swaping values "+a+ " "+b );
		
	}
	
	
	
	
	
	
}
